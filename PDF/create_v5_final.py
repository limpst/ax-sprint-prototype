def body(text, bold=False, italic=False, indent_level=0):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.font.size = Pt(10)
    if bold:   r.bold = True
    if italic: r.italic = True
    p.paragraph_format.left_indent = Cm(indent_level * 0.7)
    p.paragraph_format.space_after  = Pt(3)
    return p

def bullet(text, level=1):
    p = doc.add_paragraph(style='List Bullet')
    # strip leading bullets
    text = re.sub(r'^[-·•]\s*', '', text)
    r = p.add_run(text)
    r.font.size = Pt(10)
    p.paragraph_format.left_indent = Cm(level * 0.7)
    p.paragraph_format.space_after  = Pt(2)
    return p

def code_block(text):
    p = doc.add_paragraph()
